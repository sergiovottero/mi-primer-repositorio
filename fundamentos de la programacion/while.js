// EL BUCLE WHILE SE UTILIZA CUANDO NO SE SABE CUANTAS ITERACIONES VA A TENER NUESTRO PROGRAMA

var x=10                                        // valor inicial de x

while (x>0) {                                   // condicion
    console.log ("Mi variable vale: " +x);      // sentencia o bloque de codigo que se va a ejecutar
    x--                                         // posteriormente le estoy diciendo que le reste 1 a X
}                                             

// se va a ejecutar el bloque de codigo hasta que la condicion sea verdadera